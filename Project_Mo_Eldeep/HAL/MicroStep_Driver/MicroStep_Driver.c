// Function to enable/disable the driver
void Enable_TB6600(void)
 {
	// Set ENABLE_PIN low to enable driver
	 DIO_WriteChannel(ENABLE_PIN , PIN_LOW);
	 

}

void SetDirection_TB600(uint8 DIRECTION)
{
	switch(DIRECTION)
	{
		case COUNTER_CLOCKWISE:
			 DIO_WriteChannel(DIR_PIN , PIN_LOW);

		break;
		
		case CLOCKWISE:
			 
			 DIO_WriteChannel(DIR_PIN , PIN_HIGH);

		break;
		default:
		break;
	}


}
void Disable_TB6600(void)
{
	// Set ENABLE_PIN high to disable driver
	 DIO_WriteChannel(ENABLE_PIN , PIN_HIGH);
}

// Function to rotate stepper motor
void Pul_TB6600(void)
 {
	 DIO_WriteChannel(PORTB_PIN1 , PIN_HIGH);
	 _delay_ms(2);
	 DIO_WriteChannel(PORTB_PIN1 , PIN_LOW);
	 _delay_ms(2);

	
	
}



